/**
 * Telemetry AI — typy i interfejsy.
 *
 * Moduł telemetry jest pasywny — solver JS pozostaje jedynym źródłem prawdy
 * dla doboru elementów studni. AI w przyszłości jedynie analizuje zgromadzone
 * dane i rekomenduje wybór spośród technicznie poprawnych konfiguracji.
 */

/** Źródło konfiguracji */
export type SolverSource = 'AUTO_JS' | 'AUTO_PYTHON' | 'MANUAL' | 'AI_SUGGEST';

/** Status wersji konfiguracji */
export type ReviewStatus = 'active' | 'archived' | 'shadowed';

/** Event typy */
export type TelemetryEventType =
    | 'auto_run'
    | 'user_change'
    | 'accept'
    | 'reject'
    | 'save_offer'
    | 'create_order'
    | 'telemetry_reason'
    | 'rule_violation'
    | 'fallback_triggered';

/** Komponent studni (pojedynczy element w config) */
export interface WellComponentSnapshot {
    productId: string;
    productName?: string;
    componentType: string;
    dn?: string | number;
    height?: number;
    width?: number;
    position?: number;
    isReduction?: boolean;
    isKonus?: boolean;
    isHatch?: boolean;
    isSeal?: boolean;
}

/** Przejście szczelne */
export interface TransitionSnapshot {
    transitionNo: number;
    dn: string;
    transitionType?: string;
    producer?: string;
    heightFromBottomMm?: number;
    angleDeg?: number;
    position?: string;
    collided?: boolean;
    affectedDennicaHeight?: number;
    affectedRingSelection?: boolean;
    affectedReductionChoice?: boolean;
    affectedFinalConfig?: boolean;
}

/** Pełny payload telemetry — struktura wejściowa do API */
export interface TelemetryConfigPayload {
    // Kontekst studni (opcjonalny)
    offerId?: string;
    wellId?: string;
    clientId?: string;
    projectId?: string;
    warehouse?: string;

    // Parametry wejściowe studni
    dn?: string;
    rzDna?: number;
    rzWlazu?: number;
    wellHeight?: number;
    wellType?: string;
    terminationType?: string;
    reductionType?: string;
    zwiencenieType?: string;
    dennicaType?: string;
    dennicaHeight?: number;
    ringCount?: number;
    ringHeights?: number[];

    // Komponenty (snapshot wszystkich wybranych)
    appliedReductions?: WellComponentSnapshot[];
    appliedKonus?: WellComponentSnapshot[];
    appliedHatches?: WellComponentSnapshot[];
    appliedSeals?: WellComponentSnapshot[];
    allComponentIds?: string[];

    // Solver metadata
    solverSource: SolverSource;
    solverVersion?: string;
    rulesVersion?: string;
    computationMs?: number;
    iterationCount?: number;
    checkedVariants?: number;
    rankingScore?: number;
    selectionReason?: string;

    // Boolean flagi
    wasAutoGenerated?: boolean;
    wasAccepted?: boolean;
    wasRejected?: boolean;
    wasModified?: boolean;
    modificationCount?: number;
    confidenceScore?: number;
    learningWeight?: number;

    // Wersjonowanie
    configVersion?: number;
    parentConfigId?: string;
    reviewStatus?: ReviewStatus;

    // Features/labels (pod przyszłe AI)
    featureSnapshot?: Record<string, unknown>;
    labelSnapshot?: Record<string, unknown>;
    predictionSnapshot?: Record<string, unknown> | null;

    // Przejścia szczelne — wydzielone od zwykłych komponentów
    transitions?: TransitionSnapshot[];

    // Original vs final config (korekty użytkownika)
    originalConfig?: WellComponentSnapshot[];
    finalConfig?: WellComponentSnapshot[];
    overrideReason?: string;
}

/** Event telemetry (pojedyncza akcja) */
export interface TelemetryEventInput {
    telemetryId?: string;
    eventType: TelemetryEventType;
    wellId?: string;
    componentId?: string;
    previousValue?: string;
    newValue?: string;
    changeReason?: string;
    msSinceConfig?: number;
    orderInSession?: number;
    sequenceNo?: number;
}

/** Model pełnej historii wersji konfiguracji */
export interface ConfigHistoryInput {
    wellId: string;
    configVersion: number;
    parentId?: string;
    configJson: Record<string, unknown>;
    source: SolverSource;
    triggeredBy?: string;
    diffFromParent?: WellComponentSnapshot[];
    rankingScore?: number;
    selectionReason?: string;
}

/** Wersje solvera/reguł/AI */
export interface VersionRegistrationInput {
    componentType: 'solver' | 'rules' | 'ai' | 'embedding';
    version: string;
    description?: string;
    schemaVersion?: string;
    isActive?: boolean;
}

/** Odpowiedź API z zapisu telemetry */
export interface TelemetryRecordResponse {
    success: boolean;
    telemetryId: string;
    configHistoryId?: string;
    eventsCreated?: number;
    transitionsCreated?: number;
}

/** Odpowiedź API z list telemetry */
export interface TelemetryListResponse<T = unknown> {
    items: T[];
    total: number;
    page?: number;
    pageSize?: number;
}

/** Wagi preferencji (placeholder dla przyszłego AI) */
export interface PreferenceWeights {
    substitution: Map<string, { replaceWith: string; weight: number; confidence: number }>;
    addition: Map<string, { weight: number; confidence: number }>;
    removal: Map<string, { weight: number; confidence: number }>;
}

/** Config wejściowy dla pasywnego hooka w solverze JS */
export interface PassiveHookData {
    configId?: string;
    wellId?: string;
    iterations?: number;
    variants?: number;
    transitionsCount?: number;
    collisionsCount?: number;
    fallbackUsed?: boolean;
    rankingScore?: number;
    selectionReason?: string;
    msStart?: number;
}
