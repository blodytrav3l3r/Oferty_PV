-- nowe tabele
CREATE TABLE IF NOT EXISTS "ai_telemetry_events" (
    "id" TEXT NOT NULL PRIMARY KEY,
    "telemetryId" TEXT,
    "eventType" TEXT NOT NULL,
    "userId" TEXT,
    "wellId" TEXT,
    "componentId" TEXT,
    "previousValue" TEXT,
    "newValue" TEXT,
    "changeReason" TEXT,
    "msSinceConfig" INTEGER,
    "orderInSession" INTEGER,
    "sequenceNo" INTEGER DEFAULT 0,
    "createdAt" TEXT
