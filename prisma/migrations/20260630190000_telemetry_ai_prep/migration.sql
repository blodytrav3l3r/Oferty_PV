CREATE TABLE IF NOT EXISTS "ai_config_history" (
    "id" TEXT NOT NULL PRIMARY KEY,
    "wellId" TEXT,
    "configVersion" INTEGER NOT NULL,
    "parentId" TEXT,
    "configJson" TEXT,
    "source" TEXT,
