    "id" TEXT NOT NULL PRIMARY KEY,
    "componentType" TEXT NOT NULL,
    "version" TEXT NOT NULL,
    "description" TEXT,
    "schemaVersion" TEXT,
    "isActive" BOOLEAN NOT NULL DEFAULT true,
    "appliedFrom" TEXT,
    "createdAt" TEXT
);

CREATE TABLE IF NOT EXISTS "ai_transition_snapshots" (
    "id" TEXT NOT NULL PRIMARY KEY,
    "wellId" TEXT,
    "configId" TEXT,
    "transitionNo" INTEGER,
    "dn" TEXT,
    "transitionType" TEXT,
    "producer" TEXT,
    "heightFromBottomMm" REAL,
    "angleDeg" REAL,
    "position" TEXT,
    "collided" BOOLEAN NOT NULL DEFAULT false,
    "affectedDennicaHeight" REAL,
    "affectedRingSelection" BOOLEAN NOT NULL DEFAULT false,
    "affectedReductionChoice" BOOLEAN NOT NULL DEFAULT false,
    "affectedFinalConfig" BOOLEAN NOT NULL DEFAULT false,
    "minimalDennicaForTransitionsMm" REAL,
    "solverModifiedForTransitions" BOOLEAN NOT NULL DEFAULT false,
    "createdAt" TEXT
);

